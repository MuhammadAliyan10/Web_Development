//! It is same like condition but in this use case instead of if else

opr = "+"
switch (opr) {
    case "+":
        console.log(a + b);

        break;
    case "-":
        console.log(a - b);

        break;
    case "*":
        console.log(a * b);

        break;
    case "/":
        console.log(a / b);
        break;

    default:
        console.log("Invalid opreater");
        break;
}